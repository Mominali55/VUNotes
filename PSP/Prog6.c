// using n++ and ++n
#include<stdio.h>

int main()
{
    int n=5;
    printf("If used n++:%d \n",n++); //It will print "n" value as it states n=n+1 so "n" will be printed first.
    int a=5;
    printf("if used ++a:%d",++a); //It will print "a+1" value as it states a+1=a so "a+1" will be printed first.
}